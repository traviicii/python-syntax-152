## Naming Conventions

# snake_case
    # Python standard naming convention
    # always lowercase
    # always underscored between words

# camelCase
    # JavaScript naming convention
    # first word, always lowercase
    # Capitalize the first letter of each word that follows

# PascalCase
    # similar to camelCase but with every first letter of each word is capitalized

# kebab-case
    # every-word-is-seperated-by-a-dash

# ALL CAPS
    # represents a piece of data that will never change

NUM_PI = 3.14

